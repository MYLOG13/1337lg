#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	nprint(int n, int n1, int n2)
{
	ft_putchar(n + 48);
	ft_putchar(n1 + 48);
	ft_putchar(n2 + 48);
}

void	commaspace(void)
{
	ft_putchar(',');
	ft_putchar(' ');
}

void	ft_print_comb(void)
{
	int	i;
	int	j;
	int	k;

	i = 0;
	j = 0;
	k = 0;
	while (i <= 7)
	{
		j = i + 1;
		while (j <= 8)
		{
			k = j + 1;
			while (k <= 9)
			{
				nprint(i, j, k);
				if (i != 7 || j != 8 || k != 9)
					commaspace();
				k++;
			}
			j++;
		}
		i++;
	}
}

int	main(void)
{
	ft_print_comb();
	return (0);
}
